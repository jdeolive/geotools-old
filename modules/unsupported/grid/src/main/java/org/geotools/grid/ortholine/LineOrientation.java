/**
 *
 * @author michael
 */
public enum LineOrientation {
    /**
     * Line parallel to North-South axis in geographic space or
     * Y-axis in Cartesian space.
     */
    VERTICAL, 
    
    /**
     * Line parallel to EAST-WEST axis in geographic space or
     * X-axis in Cartesian space.
     */ 
    HORIZONTAL
    
}
